import { SYSTEM_BASE, userWrap } from "./base";
import type { DiscourseResult } from "@/types/analysis";

export function buildDiscoursePrompt(text: string): {
  system: string;
  user: string;
} {
  const user = `${userWrap(text)}

Проведи научный дискурс-анализ текста. Определи ключевые прагматические характеристики.

Верни JSON строго такой структуры:
{
  "communicativeGoal": "коммуникативная цель (что автор хочет достичь)",
  "discourseType": "тип дискурса (напр.: научный, публицистический, рекламный, политический, художественный, бытовой, религиозный и т.д.)",
  "speechStrategy": "речевая стратегия (глобальный замысел: информирование, убеждение, манипуляция, самопрезентация и т.д.)",
  "speechTactic": "речевая тактика (конкретные приёмы реализации стратегии)",
  "targetAudience": "предполагаемая аудитория (соц., проф., возрастная характеристики)",
  "modality": "модальность (аффективная, деонтическая, эпистемическая; окраска: уверенность, долженствование, возможность)",
  "register": "регистр общения (высокий/средний/низкий; формальный/неформальный)",
  "notes": ["1–3 дополнительных наблюдения о структуре дискурса"]
}

Каждое текстовое поле — 1–2 предложения научным языком.`;
  return { system: SYSTEM_BASE, user };
}

export type { DiscourseResult };
